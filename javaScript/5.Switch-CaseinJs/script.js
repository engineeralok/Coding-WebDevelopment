const grade = 'A';

switch (grade) {
    case 'A':
        console.log('A -> very good');
        break;

    case 'B':
        console.log('B -> good, keep going');
        break;

    case 'C':
        console.log('C -> Need Improvements');
        break;

    case 'D':
        console.log('D -> very bad');
        break;

    default:
        console.log('Invalid grade');
}

